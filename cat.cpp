class Frame
{
  public:
    Frame( string data )
      : m_Data( DoubleSize( data ) )
    {
    }

    void Draw() const
    {
        cout << m_Data;
    }

  private:
    string DoubleSize( string v )
    {
        stringstream result = {};
        for_each( v.begin(),
                  v.end(),
                  [ & ]( char c )
                  {
                      result << Colorize( c );
                      if ( c != '\n' )
                          result << Colorize( c );
                  } );
        return result.str();
    }

    std::string Colorize( char c )
    {
        switch ( c )
        {
            default:
                return string( 1, c );
        }
    }

  private:
    string m_Data;
};

class Frames
{
  public:
    Frames()
      : m_Frames()
      , m_CurFrame( 0 ) {};

    template <class T>
    void PushFrame( T &&v )
    {
        m_Frames.push_back( forward<T>( v ) );
    }

    void Render()
    {
        Clear();
        m_Frames[ m_CurFrame ].Draw();
        cout.flush();
        m_CurFrame = ( m_CurFrame + 1 ) % m_Frames.size();
    }

  private:
    void Clear()
    {
        system( "clear" );
    }

  private:
    vector<Frame> m_Frames   = {};
    int           m_CurFrame = -1;
};

class FpsLimiter
{
  public:
    FpsLimiter() = delete;

    explicit FpsLimiter( const float fTargetMs )
      : m_fTarget( fTargetMs )
      , m_fBalance( 0.f )
    {
    }

    ~FpsLimiter() = default;

  public:
    FpsLimiter( const FpsLimiter & ) noexcept            = default;
    FpsLimiter &operator=( const FpsLimiter & ) noexcept = default;

    FpsLimiter( FpsLimiter && ) noexcept            = default;
    FpsLimiter &operator=( FpsLimiter && ) noexcept = default;

  public:
    float GetTarget() const
    {
        return m_fTarget;
    }

  public:
    float Block( const float fDeltaMs, const float fFetchMs )
    {
        using DurationMs = chrono::duration<float, milli>;

        m_fBalance += m_fTarget - fFetchMs;
        const float fTotalWait = m_fTarget - fDeltaMs + m_fBalance;
        if ( fTotalWait <= 0.f )
        {
            m_fBalance *= 0.5f;
            return 0.f;
        }

        this_thread::sleep_for( DurationMs( fTotalWait ) );

        return fTotalWait;
    }


  private:
    float m_fTarget  = -1.f;
    float m_fBalance = -1.f;
};

